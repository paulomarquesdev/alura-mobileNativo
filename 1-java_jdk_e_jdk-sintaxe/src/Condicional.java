public class Condicional {
    public static void main(String[] args) {
        int age = 22;
        Boolean eDeMaior = age>=18;
        
        // Condicional simples
        if (eDeMaior) {
            System.out.println("Você é maior de idade");
        } else {
            System.out.println("Você não é maior de idade");
        }

        // Operadores logicos
        // && - e
        // || - ou
        // ! - não
    }
}
